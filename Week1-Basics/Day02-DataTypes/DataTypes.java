// day 2 - data types
// learned different types of variables in java
// int for numbers, string for text, boolean for true/false

public class DataTypes {
    public static void main(String[] args) {
        int age = 20;
        float marks = 95.5f; // f is needed at end for float
        double price = 999.99;
        char grade = 'A'; // single quotes for char
        boolean isStudent = true;
        String name = "Kiran"; // double quotes for string

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Marks: " + marks);
        System.out.println("Grade: " + grade);
        System.out.println("Is Student: " + isStudent);
    }
}
